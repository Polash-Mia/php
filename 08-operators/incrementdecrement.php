<?php

$a=10;
echo ++$a;   //pre_increment
echo "<br/>";
echo $a;
echo "<br/>";


$b=10;
echo $b++;   //post_increment
echo "<br/>";
echo $b;
echo "<br/>";


$c=15;
echo --$c;   //pre_decrement
echo "<br/>";
echo $c;
echo "<br/>";



$d=15;
echo $d--;   //post_decrement
echo "<br/>";
echo $d;
echo "<br/>";




?>